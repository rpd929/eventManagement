    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        String userName = nameField.getText();
        String email = emailField.getText();
        String pw = jPasswordField1.getText();
        if(EventBooking.checkEmail(email) && EventBooking.checkStrings(userName))
        {
            if(Storage.checkUser(EventBooking.currentUser.email, pw))
            {   
                updateUser(userName, email);
                this.dispose();
                new mainMenu(EventBooking.currentUser).setVisible(true);
            } else{
                new errorForm("Incorrect Password").setVisible(true);
            }
        }

    }//GEN-LAST:event_jButton1ActionPerformed

