    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       
         DefaultListModel<String> model = new DefaultListModel<>();
         for(int x = 0; x < EventBooking.currentUser.eventHistory.size(); x++)
         {
             model.add(x, EventBooking.currentUser.eventHistory.get(x).getEventString());
         }
        if(EventBooking.currentUser.eventHistory.isEmpty())
        {
          new errorForm("No events to view...").setVisible(true);
         
        } else {
            jList1.setModel(model);
        }
       
      

    }//GEN-LAST:event_jButton1ActionPerformed

